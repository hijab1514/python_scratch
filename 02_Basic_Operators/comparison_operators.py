"""
Comparison Operators in Python
------------------------------
Used to compare two values. Returns a boolean result (True or False).
"""

# Variables
x = 7
y = 10

# Equal to
print("x == y:", x == y)

# Not equal to
print("x != y:", x != y)

# Greater than
print("x > y:", x > y)

# Less than
print("x < y:", x < y)

# Greater than or equal to
print("x >= y:", x >= y)

# Less than or equal to
print("x <= y:", x <= y)

